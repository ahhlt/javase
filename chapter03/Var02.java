// 使用变量模拟人的信息

public class Var02{
	// 主方法，程序的入口
	public static void main(String[] args){
		
		// 记录人的信息
		int age = 25;
		double score = 88.8;
		char gender = '男';
		String name = "赵帅博";
		// 输出信息
		System.out.println("人的信息如下：");
		System.out.println("name = " + name);
		System.out.println("gender = " + gender);
		System.out.println("age = " + age);
		System.out.println("score = " + score);
	}
}